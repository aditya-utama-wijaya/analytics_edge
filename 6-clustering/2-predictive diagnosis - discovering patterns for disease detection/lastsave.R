# logistic regression, CART, and random forest are all designed to be used to predict an outcome like whether or not someone has a heart attack, since this is a classification problem
# linear regression would be appropriate for a problem with a continuous outcome, such as the amount of time until someone has a heart attack

# k-means clustering differs from hierarchical clustering in a couple important ways
# in k-means clustering, you have to pick the number of clusters before you run the algorithm, but the computational effort needed is much less than that for hierarchical clustering

# the clusters can be used to fing interesting patterns in addition to being used to improve predictive models
# by changing the number of clusters, you can find more general or more specific patterns
# if you wanted to find more unusual patterns, you would increase the number of clusters, since the clusters would become smaller and more patterns would probably emerge
